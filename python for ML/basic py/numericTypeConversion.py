# coercing integers to floats and vice versa
# We can convert one type of number into another. This is also known as coercion.

a = 100
print(a, " is a type of ", type(a))
print("now convert inot int ", float(a))  # convert integer into float

b = 19.12
print(b, " is a type of ", type(b))
print("now convert into int ", int(b))
