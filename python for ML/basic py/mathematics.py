# Python offers modules like math and random to carry out different mathematics like trigonometry, logarithms, probability and statistics, etc.

import math as m
import random as r
print(m.pi)  # sows the value of pi
print(m.cos(m.pi))  # value of cos pi
print(m.exp(1))
print(m.log(2))

print(m.factorial(5))  # factorial

# random is used to generate random no.or alphabets
x = ['a', 'b', 'c', 'd', 'e']
print(r.shuffle(x))
print(r.choice(x))
